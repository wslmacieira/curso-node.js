-- Cria a tabela heroes
DROP TABLE IF EXISTS TB_HEROE
CREATE TABLE TB_HEROES (
  ID INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY NOT NULL,
  NOME TEXT NOT NULL,
  PODER TEXT NOT NULL
)

-- create
INSERT INTO TB_HEROES
  (NOME, PODER)
